int	ft_sqrt(int nb)
{
	long	sqrt;

	sqrt = 0;
	if (nb % 10 == 2 || nb % 10 == 3 || nb % 10 == 7 || nb % 10 == 8)
		return (0);
	while (nb >= sqrt * sqrt)
	{
		if (nb == sqrt * sqrt)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
